n = int(input())
s1 = []
s2 = []
counter = 0
for _ in range(n):
    s1.append(input())
k = int(input())
for _ in range(k):
    s2.append(input())
for i in range(n):
    for q in range(k):
        if s2[q].lower() in s1[i].lower():
            counter += 1
    if counter == k:
        print(s1[i])
    counter = 0
